# 고성능 YOLO ByteTrack 기반 실시간 가공 코어 태스크
def blur_video_processing_task(video_id: int, input_path: str, local_output_path: str, drive_final_path: str, registered_people: list):
    cap = cv2.VideoCapture(input_path)
    if not cap.isOpened():
        write_status_file(video_id, "FAILED", "영상 파일을 열 수 없습니다.")
        return

    width, height = int(cap.get(cv2.CAP_PROP_FRAME_WIDTH)), int(cap.get(cv2.CAP_PROP_FRAME_HEIGHT))
    fps = cap.get(cv2.CAP_PROP_FPS)
    if width > 1280 or height > 720: width, height = 1280, 720

    local_raw_path = local_output_path.replace(".mp4", "_raw.mp4")
    out = cv2.VideoWriter(local_raw_path, cv2.VideoWriter_fourcc(*"mp4v"), fps, (width, height))

    identity_memory = {}
    device = 0 if torch.cuda.is_available() else "cpu"

    print(f"\n[AI 가동] Video ID {video_id} 연산 루프 주행 가동...")

    try:
        while cap.isOpened():
            ret, frame = cap.read()
            if not ret: break
            frame = cv2.resize(frame, (width, height))

            # 1. YOLO ByteTrack 얼굴 탐지 및 동적 스캔
            face_res = face_model.track(frame, conf=face_conf, imgsz=face_imgsz, device=device, persist=True, verbose=False)
            if face_res and len(face_res) > 0 and face_res[0].boxes is not None and face_res[0].boxes.id is not None:
                try:
                    xywh_list = face_res[0].boxes.xywh.cpu().numpy()
                    track_ids = face_res[0].boxes.id.int().cpu().numpy()

                    for xywh, t_id in zip(xywh_list, track_ids):
                        cx, cy, bw, bh = xywh
                        radius = int(max(bw, bh) / 2 * radius_scale_face)
                        radius = max(radius, min_face_radius)

                        if t_id in identity_memory:
                            action = identity_memory[t_id]["action"]
                            emoji_img = identity_memory[t_id]["emoji_img"]
                        else:
                            margin = int(radius * 0.40)
                            x1_f, y1_f = max(0, int(cx - radius - margin)), max(0, int(cy - radius - margin))
                            x2_f, y2_f = min(width, int(cx + radius + margin)), min(height, int(cy + radius + margin))
                            face_crop = frame[y1_f:y2_f, x1_f:x2_f]

                            action, emoji_img = "blur", None

                            if face_crop.size > 0 and len(registered_people) > 0:
                                face_crop_resized = cv2.resize(face_crop, (160, 160), interpolation=cv2.INTER_CUBIC)
                                rgb_face = cv2.cvtColor(face_crop_resized, cv2.COLOR_BGR2RGB)
                                encodings = face_recognition.face_encodings(rgb_face, model="hog")

                                if len(encodings) == 0:
                                    encodings = face_recognition.face_encodings(rgb_face, model="cnn")

                                if len(encodings) > 0:
                                    face_encoding = encodings[0]
                                    distances = [np.min(face_recognition.face_distance(p["encodings"], face_encoding)) for p in registered_people]
                                    best_idx = np.argmin(distances)

                                    if distances[best_idx] <= 0.48:
                                        action = registered_people[best_idx]["action"]
                                        emoji_img = registered_people[best_idx]["emoji_img"]
                                        print(f"트래커 ID {t_id} -> 액션: {action}")

                            identity_memory[t_id] = {"action": action, "emoji_img": emoji_img}

                        if action == "no_blur": continue
                        elif action == "emoji": overlay_emoji(frame, cx, cy, radius, emoji_img)
                        else: blur_circle_roi(frame, cx, cy, radius)
                except Exception as tracker_err:
                    pass

            # 2. 원거리 자동차 번호판 스캔 루프
            lp_res = lp_model.predict(frame, conf=lp_conf, imgsz=lp_imgsz, device=device, verbose=False)
            if lp_res and len(lp_res) > 0 and lp_res[0].boxes is not None:
                try:
                    lp_xywh = lp_res[0].boxes.xywh.cpu().numpy()
                    for cx_lp, cy_lp, bw_lp, bh_lp in lp_xywh:
                        if bw_lp <= 0 or bh_lp <= 0 or (bw_lp / bh_lp) < 1.8: continue
                        radius_lp = int(bw_lp / 2 * radius_scale_lp)
                        blur_rect_roi(frame, cx_lp, cy_lp, max(radius_lp, min_lp_radius))
                except Exception as lp_err:
                    pass

            out.write(frame)

        cap.release()
        out.release()

        # FFMPEG 웹표준 인코딩 트랜스포밍 단계
        print(f"[서버] 가공 완료. 브라우저 규격 H.264 트랜스포밍 개시...")
        ffmpeg_cmd = f"ffmpeg -y -i {local_raw_path} -vcodec libx264 -pix_fmt yuv420p -profile:v high -level:v 4.0 -an {local_output_path}"
        os.system(ffmpeg_cmd)

        shutil.copy(local_output_path, drive_final_path)
        if os.path.exists(local_raw_path): os.remove(local_raw_path)

        write_status_file(video_id, "COMPLETED")
        print(f"[성공] 작업 ID {video_id} 최종 완전체 비디오 출력 성공 \n")

    except Exception as e:
        if 'cap' in locals(): cap.release()
        if 'out' in locals(): out.release()
        print(f"[에러 발생] 원인: {str(e)}")
        write_status_file(video_id, "FAILED", str(e))



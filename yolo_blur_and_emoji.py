video_id_counter = 1

@app.post("/api/blur")
async def upload_and_blur(
    file: UploadFile = File(...),
    options: str = Form(...),
    person_images: list[UploadFile] = File([]),
    emoji_images: list[UploadFile] = File([]),
    background_tasks: BackgroundTasks = None
):
    global video_id_counter
    current_id = video_id_counter
    video_id_counter += 1

    local_input_path = os.path.join(LOCAL_INPUT_DIR, f"input_{current_id}.mp4")
    local_output_path = os.path.join(LOCAL_OUTPUT_DIR, f"output_{current_id}.mp4")
    drive_final_path = os.path.join(DRIVE_OUTPUT_DIR, f"output_{current_id}.mp4")

    with open(local_input_path, "wb") as buffer:
        shutil.copyfileobj(file.file, buffer)

    parsed_options = json.loads(options)
    person_img_dict = {f.filename: await f.read() for f in person_images}

    dynamic_registered_people = []
    for opt in parsed_options:
        person_id = opt.get("person_id")
        action = opt.get("action", "blur")
        img_filename = opt.get("image_filename")
        emoji_filename = opt.get("emoji_filename", "cat.png")

        if img_filename not in person_img_dict: continue

        encodings = augment_and_encode_image(person_img_dict[img_filename])
        if not encodings: continue

        emoji_cv_img = None
        if action == "emoji" and emoji_filename:
            drive_emoji_path = os.path.join(DRIVE_EMOJI_DIR, emoji_filename)
            if os.path.exists(drive_emoji_path):
                emoji_cv_img = cv2.imread(drive_emoji_path, cv2.IMREAD_UNCHANGED)
            else:
                backup_path = os.path.join("/content/drive/MyDrive", emoji_filename)
                if os.path.exists(backup_path):
                    emoji_cv_img = cv2.imread(backup_path, cv2.IMREAD_UNCHANGED)

        dynamic_registered_people.append({
            "person_id": person_id,
            "action": action,
            "encodings": encodings,
            "emoji_img": emoji_cv_img
        })

    write_status_file(current_id, "PROCESSING")
    background_tasks.add_task(blur_video_processing_task, current_id, local_input_path, local_output_path, drive_final_path, dynamic_registered_people)
    return {"video_id": current_id, "status": "PROCESSING"}


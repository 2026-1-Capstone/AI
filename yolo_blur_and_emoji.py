# 2. 비식별화 그래픽스 엔진 헬퍼 함수들
def blur_circle_roi(frame, cx, cy, radius):
    h, w = frame.shape[:2]
    x1, y1 = max(0, int(cx - radius)), max(0, int(cy - radius))
    x2, y2 = min(w, int(cx + radius)), min(h, int(cy + radius))
    if x2 <= x1 or y2 <= y1: return
    roi = frame[y1:y2, x1:x2]
    mask = np.zeros((roi.shape[0], roi.shape[1]), dtype=np.uint8)
    cv2.circle(mask, (int(cx - x1), int(cy - y1)), int(radius), 255, -1)
    blurred = cv2.GaussianBlur(roi, (blur_kernel, blur_kernel), 0)
    roi[mask == 255] = blurred[mask == 255]

def overlay_emoji(frame, cx, cy, radius, emoji_img):
    if emoji_img is None:
        blur_circle_roi(frame, cx, cy, radius)
        return
    h, w = frame.shape[:2]
    size = int(radius * 2.5)
    x1, y1 = max(0, int(cx - size // 2)), max(0, int(cy - size // 2))
    x2, y2 = min(w, int(cx + size // 2)), min(h, int(cy + size // 2))
    roi_w, roi_h = x2 - x1, y2 - y1
    if roi_w <= 0 or roi_h <= 0: return

    emoji_resized = cv2.resize(emoji_img, (roi_w, roi_h), interpolation=cv2.INTER_AREA)
    if emoji_resized.shape[2] == 4:
        alpha_mask = emoji_resized[:, :, 3] / 255.0
        for c in range(3):
            frame[y1:y2, x1:x2, c] = (emoji_resized[:, :, c] * alpha_mask + frame[y1:y2, x1:x2, c] * (1.0 - alpha_mask)).astype(np.uint8)
    else:
        frame[y1:y2, x1:x2] = emoji_resized[:, :, :3]

def blur_rect_roi(frame, cx, cy, radius):
    h, w = frame.shape[:2]
    x1, y1 = max(0, int(cx - radius * 1.2)), max(0, int(cy - radius * 0.45))
    x2, y2 = min(w, int(cx + radius * 1.2)), min(h, int(cy + radius * 0.45))
    if x2 <= x1 or y2 <= y1: return
    roi = frame[y1:y2, x1:x2]
    if roi.size > 0:
        frame[y1:y2, x1:x2] = cv2.GaussianBlur(roi, (blur_kernel, blur_kernel), 0)

def augment_and_encode_image(image_bytes):
    nparr = np.frombuffer(image_bytes, np.uint8)
    image = cv2.imdecode(nparr, cv2.IMREAD_COLOR)
    if image is None: return []
    rgb_image = cv2.cvtColor(image, cv2.COLOR_BGR2RGB)
    encodings = face_recognition.face_encodings(rgb_image, model="cnn")
    if len(encodings) == 0:
        encodings = face_recognition.face_encodings(rgb_image, model="hog")
    return encodings


